%% Func: start/2
%% Returns: {ok, Pid}        |
%%          {ok, Pid, State} |
%%          {error, Reason}
%% --------------------------------------------------------------------
start(_Type, _StartArgs) ->
	{ok, LSock} = gen_tcp:listen(8583, [{active, true}]),	
    case node8583_sup:start_link(LSock) of
	{ok, Pid} ->
	    {ok, Pid};
	Other ->
	    {error, Other}
    end.

%% --------------------------------------------------------------------
%% Func: stop/1
%% Returns: any
%% --------------------------------------------------------------------
